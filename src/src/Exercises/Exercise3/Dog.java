package Exercises.Exercise3;

public class Dog {
    // creation of a class Constructor.

    // the fields here represent the properties of the object.
    // while functions represent the methods of the object.
    String name;
    int age;

    // this is how we create the constructor of a class.
    // the constructor method's name is the same as the class name and no return type.
    public Dog(String name, int age) {
        this.name = name;
        this.age = age;
    }
}
